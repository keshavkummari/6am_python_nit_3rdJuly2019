# Example #1: Arithmetic operators in Python
x = 15
y = 4


print(x,id(x),type(x))

print("")

print(y,id(y),type(y))

print("")

# Output: x + y = 19   Addition
print('x + y =',x+y)

# Output: x - y = 11   Subtraction
print('x - y =',x-y)

# Output: x * y = 60    Multiplication
print('x * y =',x*y)

# Output: x / y = 3.75  Division
print('x / y =',x/y)

# Output: x // y = 3    Floor Division
print('x // y =',x//y)

# Output: x ** y = 50625 Exponent
print('x ** y =',x**y)
